package me.soulyana;

public class Skills {
    private String skill, skillRanking;

    public Skills() {
        this.skill = skill;
        this.skillRanking = skillRanking;
    }

    public String getSkill() {
        return skill;
    }

    public void setSkill(String skill) {
        this.skill = skill;
    }

    public String getSkillRanking() {
        return skillRanking;
    }

    public void setSkillRanking(String skillRanking) {
        this.skillRanking = skillRanking;
    }
}
